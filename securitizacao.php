			<h2>Securitização de <span>Créditos</span></h2>
			<p>Em 1997, com a promulgação da Lei Federal N°. 9.514, que dispôs sobre o Sistema de Financiamento Imobiliário – SFI e instituiu a alienação fiduciária de coisa imóvel, foram criadas as condições para o surgimento de ações mais sujeitas às forças do mercado e menos regulamentadas. Esta lei também foi responsável pela introdução de um dos principais instrumentos de securitização, o Certificado de Recebível Imobiliário.</p>
			<p>Após a regulamentação da securitização dos créditos imobiliários houve, a partir de 2002, uma série de medidas favoráveis à consolidação do mercado secundário destes créditos, tais como: as Resoluções CMN Nos. 2.922/02 e 2.967/02; a Emenda Constitucional N°.37/02; e as Leis Federais N°. 10.931/04 e 11.033/04. </p>
			<p>No ano de 2004 foi promulgada a Lei Federal N°. 10.931, responsável pela criação de dois outros instrumentos, a Cédula de Crédito Imobiliária (CCI) e a Cédula de Crédito Bancário (CCB) resultando em um aumento considerável no volume de operações de securitização. </p>
			<p>Em linhas gerais, a securitização procura reduzir o risco de uma carteira de recebíveis através da combinação de um arcabouço jurídico com ferramentas de garantias eficazes. </p>
			<p>Uma importante ferramenta de garantia é a constituição das Sociedades de Propósito Específico (SPE). Por meio da SPE é possível segregar o risco de crédito de uma carteira de recebíveis do risco de crédito do originador. Possibilita que as carteiras de recebíveis, referentes aos mais variados tipos de projetos, tornem-se independentes umas das outras e que as mesmas não sejam afetadas pelo risco de outras atividades do originador. </p>
			<p>Basicamente, a captação de recursos é feita através da emissão de títulos de crédito, ou valores mobiliários, no mercado de capitais. Por fim, a consolidação de uma operação é dada pela sua classificação de risco de crédito (Rating), junto às principais empresas de Rating do mercado.</p>
			<p>Alguns objetivos da estruturação de operações: </p>
			<ul>
				<li>Antecipação de recebíveis, inclusive dívidas de governos e royalties;</li>
				<li>Viabilização de project finance;</li>
				<li>Financiamento de empreendimentos imobiliários;</li>
				<li>Readequação de perfil de endividamento; </li>
				<li>Programas de investimentos.</li>
